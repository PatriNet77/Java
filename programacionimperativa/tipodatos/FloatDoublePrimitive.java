package programacionimperativa.tipodatos;

public class FloatDoublePrimitive {

    public static void main(String[] args) {
        
        //float tiene 32 bits
        float miMinimoValorDeFloat = Float.MIN_VALUE;
        float miMaximoValorDeFloat = Float.MAX_VALUE;

        System.out.println("Float valor mínimo: " + miMinimoValorDeFloat);
        System.out.println("Float valor máximo: " + miMaximoValorDeFloat);

        //double tiene 64 bits
        double miMinimoValorDeDouble = Double.MIN_VALUE;
        double miMaximoValorDeDouble = Double.MAX_VALUE;

        System.out.println("Double valor mínimo: " + miMinimoValorDeDouble);
        System.out.println("Double valor máximo: " + miMaximoValorDeDouble);

    }
}
