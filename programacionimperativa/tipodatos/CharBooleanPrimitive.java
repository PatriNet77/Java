package programacionimperativa.tipodatos;

public class CharBooleanPrimitive {
    
    public static void main(String[] args) {
        //char es caracter a b c d e 
        //Los caracteres se definen con '' comillas simples
        char oneChar = 'a';
        char oneCharWithUnicode = '\u0044';

        System.out.println(oneChar + " " + oneCharWithUnicode);

        //tipos booleanos
        boolean myTrue = true;
        myTrue = false;

        System.out.println(myTrue);
        
    }
}
